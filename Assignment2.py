# List
my_list = [10, 20, 30, 40, 50]
print("List:", my_list)
print("Accessing elements in the list:")
print("Element at index 1:", my_list[1])  # Second element
print("Element at index -1 (last element):", my_list[-1])  # Last element

# Tuple
my_tuple = ('apple', 'banana', 'cherry', 'date', 'elderberry')
print("\nTuple:", my_tuple)
print("Accessing elements in the tuple:")
print("Element at index 0:", my_tuple[0])  # First element
print("Element at index -2 (second-to-last element):", my_tuple[-2])  # Second-to-last element

# Dictionary
my_dict = {
    'a': 1,
    'b': 2,
    'c': 3,
    'd': 4,
    'e': 5
}
print("\nDictionary:", my_dict)
print("Accessing elements in the dictionary:")
print("Value for key 'a':", my_dict['a'])  # Value of key 'a'
print("Value for key 'd':", my_dict['d'])  # Value of key 'd')
